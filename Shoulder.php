<div id="WorkoutContainer">	
<p class="WorkoutInfo">No two people work out exactly the same. Whether it's choice and sequence of exercises, how many sets to complete of each move, how heavy to go and reps completed, length of rest periods, or advanced training techniques and finishing moves, each lifter has his or her own workout DNA.</p>	

<br>

<p class="WorkoutInfo">Pay especially close attention to how exercise choice and order, weight/rep targets, volume, and advanced-training principles can be manipulated to fine-tune a routine for a specific goal. 
If you find one you like, try it for 4-8 weeks, then go back to your regular workout or try another from the list.</p>	

<br>

<p class="WorkoutInfo">Overall Delt Mass: To build mass, always start your workout with the most demanding exercises—multijoint overhead presses, in the case of the delts—which allow you to push the most weight. In this mass workout, a second multijoint move is next, followed by single-joint moves for each of the three delts heads: front, middle, and rear. This is the foundation for a solid mass workout. 
Keeping the total volume fairly high also promotes muscle growth.</p>	

<br>

<p class="WorkoutInfo">You can make the workout more challenging in several ways. For the overhead press, start by using dumbbells, which are slightly more difficult to control and have a slightly longer range of motion than a barbell. We'll also follow a reverse-pyramid scheme, which allows you to take more total sets to muscle failure. For the first 2 sets, you'll use a fairly heavy weight for a low rep target—just 6—to promote even greater strength gains than you might normally get when shooting for a slightly higher rep target. 
As you become increasingly fatigued, decrease the weight by about 5 pounds per side on subsequent sets. Finally, on your two heaviest sets, enlist a spotter who can provide you just enough help to keep the weight moving.</p>	

<br>

<p class="WorkoutInfo">Because the front delts get so much work on chest day, and the middle delts take the brunt of the weight on overhead pressing moves, it's not uncommon for the rear delts to be the smallest—and weakest—of the three. In this workout, you'll do them before the other two, while you have a bit more energy in the tank. 
Feel free to rearrange the order of the single-joint moves based on your weaknesses, though. If you judge your delts to be fairly balanced, simply rotate the order of the single-joint movements from one workout to the next.</p>	

