<div id="WorkoutContainer">	
<p class="WorkoutInfo">It is great to have well developed biceps, but make sure you don't forget to train the rest of your muscles in the upper and lower arm with the same volume and intensity as you do your biceps.</p>	

<br>

<p class="WorkoutInfo">Many beginners fall into that trap and create terrible imbalances in their arms. Even the great Arnold Schwarzenegger admits he trained his biceps harder and more often than his triceps in the beginning. 
This built an imbalance that took him years to balance back out.</p>	

<br>

<p class="WorkoutInfo">For training your upper arms (biceps and triceps) I like to use all kinds of rep ranges, high (15-20), moderate (8-12) and low (4-6). Each has its own purpose and will be critical to use all ranges in your pursuit of bigger arms.</p>

<br>

<p class="WorkoutInfo">Forearms on the other hand are what I like to call the "burning buggers." You will have to endure some high rep muscle-burning sets to get these babies to grow. 
Reps can go as high as 50 to get these to burn like they are on fire, but it will be all worth it when you have thick meaty forearms like Popeye.</p>	

<br>

<p class="WorkoutInfo">Now that you understand which muscles make up your arms, their function, location and the rep range needed to stimulate them, let's give you some workouts to help you build your arms.</p>	

<br>

<p class="WorkoutInfo">Barbell Biceps Curl And Triceps Rope Push-Down: Start the workout by going heavier on the biceps with barbell curls. Remember to keep your back straight and avoid using momentum.
You'll increase the weight on the curls with each set, starting at 12 reps, then increasing the weight and dropping to 10 reps, then 8 and 8. On the last set, you'll finish with a triple dropset.With the rope push-downs, you'll use a lighter weight compared to the biceps curls because you're trying to pre-exhaust your triceps before giving them heavier weights at the end of the workout. 
Make sure you're going slow and controlled, completing 4 sets of 12-15 reps.</p>	

<br>

<p class="WorkoutInfo">For the cable overhead extension, do 4 sets of 15 reps, combining intra-set stretching with a dropset at the end of the final set. To do this, perform the last set of 15 reps, then hold the stretched position (arms bent) for 30 seconds. 
After the 30 seconds is up, drop the weight and go for as many reps as you can.

</p>	

