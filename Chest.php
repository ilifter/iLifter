<h1 class="WorkoutHeader"> Chest Workout</h1>

<iframe width="620" height="405" src="https://www.youtube.com/embed/ubJxrA02MLY">
</iframe>



<div id="WorkoutContainer">	
<p class="WorkoutInfo">You just want to know the best exercises for building a muscular chest, no questions asked. We've done the work for you, and found the top 10.</p>	

<br>

<p class="WorkoutInfo">This isn't a list of the hardest chest exercises. It's focused on the best-of-the-best mass builders, with a little bit of instruction and explanation to complement each choice. 
You can swap exercises in your current routine for these choices, build your own chest workout with a handful of them, or just try one when your standard chest workout gets stale.</p>	

<br>

<p class="WorkoutInfo">Barbell Bench Press. Why it's on the list: You can generate the most power with barbell lifts, so the standard barbell bench allows you to move the most weight. It's also an easier lift to control than pressing with heavy dumbbells. The exercise is easy to spot and relatively easy to learn (if not master), 
There are plenty of bench-press programs you can follow to increase your strength.</p>	



<img src="http://www.gymworkoutchart.com/wp-content/uploads/2017/06/Five-Best-Chest-Exercises.jpg" alt="" id="WorkoutPic" width="620" height="405">

<br>
<br>
<br>

<p class="WorkoutInfo">Flat Bench Dumbbell Press. Why it's on the list: With dumbbells, each side of your body must work independently, which recruits more stabilizer muscles; dumbbells are harder to control than a barbell. Dumbbells also allow for a longer range of motion than the barbell bench press, both at the bottom and top of the movement. 
Flat dumbbell presses allow you to hoist a fairly heavy weight, and they make for a good alternative if you've been stuck on the barbell bench for ages.</p>	

<br>

<p class="WorkoutInfo">In your workout: Do flat dumbbell presses toward the start of your chest workout for heavy sets in lower rep ranges.
We don't typically recommend doing dumbbell presses in addition to the barbell bench press, because both moves are so similar.</p>	

